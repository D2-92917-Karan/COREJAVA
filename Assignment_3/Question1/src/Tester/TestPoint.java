package Tester;
import com.app.geometry.Point2D;
import java.util.Scanner;

public class TestPoint {
public static void main(String[] args) {
		Scanner sc=new Scanner(System.in);
		System.out.println("Enter x and y for p1:");
		double x1=sc.nextDouble();
		double y1=sc.nextDouble();
		Point2D p1=new Point2D(x1,y1);
		
		System.out.println("Enter x and y for p2:");
		double x2=sc.nextDouble();
		double y2=sc.nextDouble();
		Point2D p2=new Point2D(x2,y2);
		
		System.out.println("p1:"+p1.getDetails());
		System.out.println("p2:"+p2.getDetails());
		
		
		if(p1.isEqual(p2)) {
			System.out.println("Point p1 and p2 are located at same position");
		}else {
			double dist=p1.calculateDistance(p2);
			System.out.println("Distance is:"+dist);
		}
}
}
